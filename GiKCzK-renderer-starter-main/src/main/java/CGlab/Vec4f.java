package CGlab;

public class Vec4f {
    public float x;
    public float y;
    public float z;
    public float w;

    public Vec4f(float x, float y, float z, float w) {
        this.x = x;
        this.y = y;
        this.z = z;
        this.w = w;
    }

    @Override
    public String toString() {
        return x + " " + y + " " + z;
    }
}